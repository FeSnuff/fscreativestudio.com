function getCurrentLanguage() {
    return localStorage.getItem('ecozeo_language') || 'es';
}

function setLanguage(lang) {
    localStorage.setItem('ecozeo_language', lang);
}

function translatePage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    console.log(`ðŸŒ [TRANSLATE] Traduciendo ${elements.length} elementos a: ${lang}`);
    
    let translated = 0;
    
    // Ocultar todos los elementos primero
    elements.forEach(element => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(20px)';
        element.style.transition = 'opacity 0.6s ease-out, transform 0.6s ease-out';
    });
    
    // Traducir y mostrar con efecto cascada
    elements.forEach((element, index) => {
        const key = element.getAttribute('data-translate');
        
        if (translations[lang] && translations[lang][key]) {
            // Traducir inmediatamente
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
            
            // Mostrar con delay escalonado (mÃ¡s espaciado para efecto cascada)
            setTimeout(() => {
                element.style.opacity = '1';
                element.style.transform = 'translateY(0)';
                translated++;
            }, index * 30); // 30ms entre cada elemento para efecto mÃ¡s notorio
        } else {
            console.warn(`âš ï¸ [TRANSLATE] Key no encontrada: ${key}`);
        }
    });
    
    console.log(`âœ… [TRANSLATE] Iniciando animaciÃ³n de ${translated} elementos`);
    
    setLanguage(lang);
    updateLanguageSelector(lang);
    
    console.log(`ðŸŒ Idioma cambiado a: ${lang}`);
}

